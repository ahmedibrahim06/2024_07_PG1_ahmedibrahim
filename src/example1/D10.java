package example1;

//switch case örnek çözümü

public class D10 {
    public static void main(String[] args) {
        int i = 30;
        switch (i) {
            case 10 : System.out.println("sayı 10 a eşit");
                break;
                case 20 : System.out.println("sayı 20 ye eşit");
                break;
                case 30 : System.out.println("sayı 30 a eşit");
                break;
            default:
                System.out.println("sayının değeri bulunamadı");
        }


    }
}
